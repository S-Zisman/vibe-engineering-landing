from fpdf import FPDF

class LandingPDF(FPDF):
    def header(self):
        pass

    def footer(self):
        self.set_y(-15)
        self.set_font("DejaVu", "", 8)
        self.set_text_color(150, 135, 112)
        self.cell(0, 10, f"vibecodingcourse.sergeyzisman.tech | {self.page_no()}", align="C")

    def section_title(self, title):
        self.ln(6)
        self.set_font("DejaVu", "B", 16)
        self.set_text_color(50, 40, 30)
        self.multi_cell(0, 9, title)
        self.ln(3)

    def body_text(self, text):
        self.set_font("DejaVu", "", 11)
        self.set_text_color(60, 55, 48)
        self.multi_cell(0, 7, text)
        self.ln(3)

    def accent_text(self, text):
        self.set_font("DejaVu", "B", 11)
        self.set_text_color(40, 35, 28)
        self.multi_cell(0, 7, text)
        self.ln(3)

    def quote_text(self, text):
        self.set_font("DejaVu", "I", 12)
        self.set_text_color(150, 135, 112)
        left = self.l_margin
        self.set_left_margin(left + 10)
        self.set_x(left + 10)
        self.multi_cell(self.w - left - 10 - self.r_margin, 7.5, text)
        self.set_left_margin(left)
        self.ln(3)

    def divider(self):
        self.ln(4)
        y = self.get_y()
        self.set_draw_color(196, 180, 152)
        self.line(30, y, self.w - 30, y)
        self.ln(6)


